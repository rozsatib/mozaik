class CorticalStimulationWithOptogeneticArray(Experiment):
    r"""
    Parent class for experiments performing optogenetic stimulation of cortical
    sheets using optical stimulator arrays.

    A stimulation pattern is generated by evaluating a signal function over each
    stimulator array. The resulting signals are applied during simulation and
    repeated *num_trials* times.

    Does not show any actual visual stimulus.

    Multiple stimulator arrays (across one or more sheets) can be used
    simultaneously, each with an independent intensity scaling factor.

    Parameters
    ----------

    model : Model
        The model on which to execute the experiment.

    Other parameters
    ----------------

    num_trials : int
        Number of times each stimulus is presented.

    stimulator_array_list : list(dict)
        List defining which stimulator arrays are used. Each entry must contain:

        sheet : str
            Name of the sheet containing the stimulator.

        name : str
            Name of the stimulator within the sheet.

        intensity_scaler : float
            Factor scaling the stimulation intensity for this stimulator.

    Notes
    -----

    Child classes must call `append_direct_stim` with a signal function of the form:

        signal_function(sheet, coor_x, coor_y, update_interval, parameters)

    which returns a 3D array of stimulation values over space and time.
    """

    required_parameters = ParameterSet(
        {
            "num_trials": int,
            "stimulator_array_list": list,
        }
    )

    def __init__(self, model, parameters):
        Experiment.__init__(self, model, parameters)
        stimulator_array_keys = {"sheet", "name", "intensity_scaler"}
        for stimulator_param in self.parameters.stimulator_array_list:
            assert (
                stimulator_param.keys() == stimulator_array_keys
            ), "Stimulator array keys must be: %s. Supplied: %s. Difference: %s" % (
                stimulator_array_keys,
                stimulator_param.keys(),
                set(stimulator_array_keys) ^ set(stimulator_param.keys()),
            )
            assert stimulator_param["intensity_scaler"] >= 0, (
                "Sheet intensity scalers for stimulator %s must be larger than 0!"
                % stimulator_param["name"]
            )

    def append_direct_stim(self, model, signal_function, signal_function_parameters):
        sfp = deepcopy(signal_function_parameters)
        signal_function = load_component(signal_function)
        stimulators, signals = OrderedDict(), []
        self.direct_stimulation = []
        for i in range(len(self.parameters.stimulator_array_list)):
            p = self.parameters.stimulator_array_list[i]
            stimulator = model.sheets[p["sheet"]].artificial_stimulators[p["name"]]
            x, y, dt = (
                stimulator.stimulator_coords_x,
                stimulator.stimulator_coords_y,
                stimulator.parameters.update_interval,
            )
            sfp["intensity"] *= p["intensity_scaler"]
            stimulators[p["sheet"]] = [
                stimulator
            ]  # In this experiment we only have a single stimulator per sheet
            signals.append(signal_function(model.sheets[p["sheet"]], x, y, dt, sfp))

        for trial in range(self.parameters.num_trials):
            self.direct_stimulation.append(stimulators)
            stimulus = InternalStimulus(
                frame_duration=sfp.duration,
                duration=sfp.duration,
                trial=trial,
                direct_stimulation_name=type(next(iter(stimulators.values()))).__name__,
                direct_stimulation_parameters=MozaikExtendedParameterSet(
                    {"signal_function_parameters": sfp}
                ),
            )
            stimulus.direct_stimulation_signals = signals
            self.stimuli.append(stimulus)


class SingleOptogeneticArrayStimulus(CorticalStimulationWithOptogeneticArray):
    r"""
    Optogenetic stimulation of cortical sheets using a single stimulation pattern.

    A stimulation pattern is generated by evaluating a user-specified signal
    function over each stimulator array. The resulting signals are applied during
    simulation and repeated *num_trials* times.

    Does not show any actual visual stimulus.

    Multiple stimulator arrays (across one or more sheets) can be used
    simultaneously, each with an independent intensity scaling factor.

    Parameters
    ----------

    model : Model
        The model on which to execute the experiment.

    Other parameters
    ----------------

    num_trials : int
        Number of times the stimulus is presented.

    stimulator_array_list : list(dict)
        List defining which stimulator arrays are used. Each entry must contain:

        sheet : str
            Name of the sheet containing the stimulator.

        name : str
            Name of the stimulator within the sheet.

        intensity_scaler : float
            Factor scaling the stimulation intensity for this stimulator.

    stimulating_signal_function : str
        Path to a function specifying the time course and spatial pattern of the
        optogenetic stimulation. The function must have the form:

            def stimulating_signal_function(sheet, coor_x, coor_y, update_interval, parameters)

        where:

        sheet : Sheet
            The Mozaik sheet to be stimulated.

        coor_x : numpy.ndarray
            X coordinates of the stimulators.

        coor_y : numpy.ndarray
            Y coordinates of the stimulators.

        update_interval : float
            Time step at which the stimulator updates.

        parameters : ParameterSet
            Additional user-defined parameters.

        The function must return a 3D numpy array of shape:

            coor_x.shape[0] x coor_x.shape[1] x (duration / update_interval)

        representing stimulation intensity over space and time.

    stimulating_signal_function_parameters : ParameterSet
        Additional parameters passed to the stimulating signal function.
    """

    required_parameters = ParameterSet(
        {
            "num_trials": int,
            "stimulator_array_list": ParameterSet,
            "stimulating_signal_function": str,
            "stimulating_signal_function_parameters": ParameterSet,
        }
    )

    def __init__(self, model, parameters):
        CorticalStimulationWithOptogeneticArray.__init__(self, model, parameters)
        self.append_direct_stim(
            model,
            self.parameters.stimulating_signal_function,
            self.parameters.stimulating_signal_function_parameters,
        )


